function add(a,b) {
    console.log(`${a} + ${b} = ${a+b}`);
    return a + b;
}

function subtract(a,b) {
    console.log(`${a} - ${b} = ${a-b}`);
    return a - b;
}

function multiply(a,b) {
    console.log(`${a} x ${b} = ${a*b}`);
    return a * b;
}

function divide(a,b) {
    console.log(`${a} / ${b} = ${a/b}`);
    return a/b;
}